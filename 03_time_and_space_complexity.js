/**
 * The absolute running time of an algorithm depends on a number of factors:
 * Programming language used
 * The computer the program runs on
 * Other prpgrams running at the same time
 * Quality of operating system
 *
 * Thus, predicting abolute running time of an algorithm is not possible
 */

/**
 * Performance of an alogrithm is evaluated in terms of its input size
 *
 * Time Complexity: Amount of time taken by an algorithm to run, as a function of input size
 * Space Complexity: Amoutn of memory taken by an algorithm to run, as a function of input size
 */

/**
 * ----------------------------------------
 * How to represent complexity?
 * ----------------------------------------
 * Using mathematical tools to represent time and space complexity:
 * Big-O Notation (O-notation) - Worst case complexity
 * Omega Notation (Ω-notattion) - Best case complexity
 * Theta Notation (Θ-notation) - Average case complexity
 */
