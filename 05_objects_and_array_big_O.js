/**
 * ----------------------------------------
 * Object Big-O
 * ----------------------------------------
 * An Object is a collection of key value pair
 * Insert - O(1)
 * Remove - O(1)
 * Access - O(1)
 * Search - O(n) [depends on no. of properties]
 * Object.keys() - O(n) [returns array of all the keys]
 * Object.values() - O(n) [returns array of all the values]
 * Object.entries() - O(n)
 */

/**
 * ----------------------------------------
 * Array Big-O
 * ----------------------------------------
 * An array is an ordered collection of values
 * Insert/Remove at end - O(1)
 * Insert/Remove at beginning- O(n) [because index needs to be reset for remaining elements]
 * Access - O(1)
 * Search - O(n) [depends on no. of elements]
 * Push/pop - O(1)
 * Shift/unshift/concat/slice/splice - O(n)
 * forEach/map/filter/reduce - O(n)
 */
