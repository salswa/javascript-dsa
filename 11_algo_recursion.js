/**
 * ----------------------------------------
 * Recursion
 * ----------------------------------------
 */

/**
 * ----------------------------------------
 * What?
 * ----------------------------------------
 * Recursion is a problem solving technique where the solution depends on
 * solutions to smaller instances of the same problem
 *
 * Recursion is when a function calls itself
 */

/**
 * ----------------------------------------
 * Why?
 * ----------------------------------------
 * A great technique to simplify you solution
 *
 * If you find yourself breaking down your problem into smaller versions
 * of the same problem, recursion is very useful
 */

/**
 * ----------------------------------------
 * Important Points:
 * ----------------------------------------
 * Every recursive solution needs to have a base case -
 * a condition to terminate the recursion.
 * Recursion might simplify solving a problem but it does not always
 * translate to a faster solution.
 * A recursive solution may be far worse compared to an iterative solution.
 */
